    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sdpi = DPItx.getText(), pn = pmtx.getText(), sn = sntx.getText(), pa = patx.getText(), sa = satx.getText(), dire = diretx.getText(), stele = telcasatx.getText(), scelu = telmoviltx.getText(), ssalario = salariotx.getText(), sbon = Bontx.getText();
        int j = 0;
        if (Bases.PostEmps == null) {
            jLabel12.setText("No hay nada en la base de datos");
            return;
        }
        if (Bases.PostEmps.isEmpty()) {
            jLabel12.setText("No hay nada en la base de datos");
        } else {
            for (int i = 0; i < Bases.PostEmps.size(); i++) {
                if (Long.parseLong(DPItx.getText()) == (Bases.PostEmps.get(i).getDPI())) {
                    j = i;
                    temp = Bases.PostEmps.get(j);
                    no_empleado = j;

                    pmtx.setText(temp.getPrimerNombre());
                    sntx.setText(temp.getSegundoNombre());
                    patx.setText(temp.getPrimerApellido());
                    satx.setText(temp.getSegundoApellido());
                    diretx.setText(temp.getDireccion());
                    telcasatx.setText(String.valueOf(temp.getTelefono()));
                    telmoviltx.setText(String.valueOf(temp.getCelular()));
                    salariotx.setText(String.valueOf(temp.getSalario()));
                    Bontx.setText(String.valueOf(temp.getBonificacion()));
                    DPItx.enableInputMethods(false);
                    jButton3.setVisible(true);

                    if (Long.parseLong(DPItx.getText()) == (Bases.General.get(j).getDPI())) {
                        no_emGen = j;
                        general = true;
                    }
                    return;
                }

            }
            for (int i = 0; i < Bases.General.size(); i++) {
                if (Long.parseLong(DPItx.getText()) == (Bases.General.get(i).getDPI())) {
                    no_emGen = i;
                    general = true;
                    break;
                }
            }

            jLabel12.setText("El regsitro no existe o no fue encontrado");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String sdpi = DPItx.getText(), pn = pmtx.getText(), sn = sntx.getText(), pa = patx.getText(), sa = satx.getText(), dire = diretx.getText(), stele = telcasatx.getText(), scelu = telmoviltx.getText(), ssalario = salariotx.getText(), sbon = Bontx.getText();
        long dpi = Long.parseLong(sdpi), tel = Long.parseLong(stele), cel = Long.parseLong(scelu);
        double salario = Double.parseDouble(ssalario), bon = Double.parseDouble(sbon);
        empleado emp = new empleado(dpi, pn, sn, pa, sa, dire, tel, cel, salario, bon);
        temp = emp;
        if (general) {
            Bases.PostEmps.set(no_empleado, temp);
            Bases.General.set(no_emGen, temp);
            Bases.escribir("PostGreSQL: Update"+emp.toString());
        } else {
            Bases.PostEmps.set(no_empleado, temp);
            Bases.General.add(temp);
            Bases.escribir("PostGreSQL: Update"+emp.toString());
        }
        jLabel12.setText("El registro se ha actualizado");
    }//GEN-LAST:event_jButton3ActionPerformed

